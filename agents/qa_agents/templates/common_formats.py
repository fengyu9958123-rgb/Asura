
"""
共享的格式定义 - 人工确认问题标准格式
"""

# 标准格式（用于PRD生成和评审）
HUMAN_CONFIRM_FORMAT = """
**人工确认格式**：

```
<HUMAN_CONFIRM_START>
问题: [要确认什么]
描述: [原需求哪里有问题、具体是什么问题、为什么需要确认]
参考示例:
- [示例1]
- [示例2]
<HUMAN_CONFIRM_END>
```

**要求**：
- 问题简短明确，可以被人工直接回答
- 描述清晰说明原需求中的位置/功能/规则，以及具体缺失、歧义或不闭环点
- 参考示例只用于帮助理解问题，不是已确认需求
- 用户可以参考示例，也可以自由填写

**示例**：
```
<HUMAN_CONFIRM_START>
问题: 需明确列表的默认排序规则
描述: PRD中提到"列表展示学校信息"，但未说明默认按什么排序，会影响开发实现和测试断言。
参考示例:
- 按创建时间倒序（最新的在前）
- 按学校名称升序（A-Z排列）
- 按更新时间倒序（最近修改的在前）
<HUMAN_CONFIRM_END>
```
"""
