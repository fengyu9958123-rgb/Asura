def create_image_analyst(config_list, template_name="默认模式"):
    """
    创建图片分析智能体
    
    Args:
        config_list: LLM配置列表
        template_name: 模板名称
        
    Returns:
        ImageAnalyst智能体实例
    """
    if template_name not in TEMPLATES:
        logger.warning(f"模板 {template_name} 不存在，使用默认模式")
        template_name = "默认模式"
    
    template = TEMPLATES[template_name]
    
    # 使用公共方法获取Vision模型配置
    vl_config = get_models_by_type(config_list, model_type="vision")
    
    # 创建智能体
    image_analyst = autogen.AssistantAgent(
        name="ImageAnalyst",
        system_message=template["system_message"],
        llm_config={
            "config_list": vl_config,
            "temperature": template["temperature"],
            "timeout": 600  # 增加到10分钟，因为VL模型处理多图片需要更长时间
        }
    )
    image_analyst.billing_config = get_selected_model_config(config_list, model_type="vision")
    
    logger.info(f"图片分析智能体创建成功，模板: {template_name}")
    return image_analyst
