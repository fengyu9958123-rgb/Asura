def create_product_manager(config_list, template_name="普通模式"):
    """创建产品经理Agent - 支持多种模板配置"""
    import logging
    logger = logging.getLogger("agents.product_manager")

    template_config = TEMPLATES.get(template_name, TEMPLATES["普通模式"])
    
    # 使用文本模型（默认）
    text_config = get_models_by_type(config_list, model_type="requirement")

    agent = autogen.AssistantAgent(
        name="ProductManager",
        system_message=template_config["system_message"],
        llm_config={
            "config_list": text_config,
            "temperature": template_config["temperature"],
            "timeout": 600,
        }
    )

    # 覆盖默认的方法以确保记录对话
    original_receive = agent.receive

    def enhanced_receive(message, sender, request_reply=None, silent=False):
        logger.info(f"ProductManager收到来自{sender.name}的消息: {message[:100]}...")
        return original_receive(message, sender, request_reply, silent)

    agent.receive = enhanced_receive
    return agent
